// WHILE LOOP
let number = 0;
while (number <= 12) {
  console.log(number);
  number = number + 2;
}

// FOR LOOP
for (let number = 0; number <= 12; number = number + 2) {
  console.log(number);
}

// 2 TO POWER OF 10
let result = 1;

for (let num = 0; num < 10; num++) {
  result = result * 2;
}

console.log(result);
